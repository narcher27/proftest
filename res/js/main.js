// radio buttons update
function updateQuestionSet() {
  if (questions[currentQuestionSet] !== undefined) {
    switch (questions[currentQuestionSet][0].type) {
      case "radio-buttons": {
        const quizContainer = document.getElementById("quizContainer");
        while (quizContainer.firstChild) {
          quizContainer.removeChild(quizContainer.firstChild);
        }

        questions[currentQuestionSet].forEach((q, qIndex) => {
          const questionContainer = document.createElement("div");
          const questionElement = document.createElement("div");
          const answersElement = document.createElement("form");

          questionElement.innerText = q.question;
          questionElement.style.marginTop = "10px";
          questionElement.style.fontWeight = "bold";

          questionContainer.appendChild(questionElement);

          q.answers.forEach((answer, aIndex) => {
            const radioButton = document.createElement("input");
            const label = document.createElement("label");

            label.style.marginRight = "10px";
            radioButton.type = "radio";
            radioButton.name = "answer" + qIndex;
            radioButton.value = aIndex;

            label.appendChild(radioButton);
            label.appendChild(document.createTextNode(answer.text));

            answersElement.appendChild(label);
          });

          questionContainer.appendChild(answersElement);
          quizContainer.appendChild(questionContainer);
        });
        break;
      }

      case "checkbox-4-text": {
        const quizContainer = document.getElementById("quizContainer");

        // Очистить предыдущие вопросы и ответы
        while (quizContainer.firstChild) {
          quizContainer.removeChild(quizContainer.firstChild);
        }

        questions[currentQuestionSet].forEach((q, qIndex) => {
          const questionContainer = document.createElement("div");
          const questionElement = document.createElement("div");
          const answersElement = document.createElement("form");

          questionElement.innerText = q.question;
          questionElement.style.margin = "10px";
          questionElement.style.fontWeight = "bold";
          questionContainer.appendChild(questionElement);

          q.answers.forEach((answer, aIndex) => {
            const checkBox = document.createElement("input");
            const label = document.createElement("label");

            checkBox.type = "checkbox";
            checkBox.name = "answer" + qIndex;
            checkBox.value = aIndex;

            label.appendChild(checkBox);
            label.appendChild(document.createTextNode(answer.text));
            label.style.display = "block";
            label.style.margin = "5px";

            answersElement.appendChild(label);
          });

          questionContainer.appendChild(answersElement);
          quizContainer.appendChild(questionContainer);
        });
        break;
      }

      default:
        break;
    }
  }
}

document.getElementById("next").addEventListener("click", function () {
  if (questions[currentQuestionSet] !== undefined) {
    switch (questions[currentQuestionSet][0].type) {
      case "radio-buttons": {
        let allAnswered = true;
        questions[currentQuestionSet].forEach((q, qIndex) => {
          const selectedAnswer = document.querySelector(
            'input[name="answer' + qIndex + '"]:checked'
          );
          if (selectedAnswer) {
            if (!userAnswers[currentQuestionSet]) {
              userAnswers[currentQuestionSet] = [];
            }
            userAnswers[currentQuestionSet][qIndex] = selectedAnswer.value;
          } else {
            allAnswered = false;
          }
        });

        if (allAnswered) {
          currentQuestionSet++;
          updateQuestionSet();
        } else {
          alert("Ответьте на все вопросы!");
        }
        break;
      }
      case "checkbox-4-text": {
        let allAnswered = true;
        let tooManyAnswers = false;

        questions[currentQuestionSet].forEach((q, qIndex) => {
          const selectedAnswers = document.querySelectorAll(
            'input[name="answer' + qIndex + '"]:checked'
          );
          if (selectedAnswers.length <= 2) {
            if (!userAnswers[currentQuestionSet]) {
              userAnswers[currentQuestionSet] = [];
            }
            userAnswers[currentQuestionSet][qIndex] = Array.from(
              selectedAnswers,
              (answer) => answer.value
            );
          } else {
            allAnswered = false;
            tooManyAnswers = true;
          }
        });

        if (allAnswered) {
          currentQuestionSet++;
          updateQuestionSet();
        } else {
          if (tooManyAnswers) {
            alert("Вы не можете выбрать больше двух ответов на один вопрос!");
          } else {
            alert("Ответьте на все вопросы!");
          }
        }
      }
    }

    // finish test
    if (questions[currentQuestionSet] === undefined) {
      displayResults();
      sendEmail("1");
      console.log(interests);
      console.log(profTypes);
      console.log(userAnswers);

    }
  }
});

