function displayResults() {
  document.getElementById("questions-screen-box").style.display = "none";
  document.getElementById("results-screen-box").style.display = "flex";
  for (let i = 0; i < keys.length; i++) {
    for (let j = 0; j < keys[i].length; j++) {
      for (let z = 0; z < keys[i][j].length; z++) {
        if (interests[keys[i][j][z][0] - 1]?.score === undefined) {
          interests[keys[i][j][z][0] - 1] = {
            ...interests[keys[i][j][z][0] - 1],
            score: 0,
          };
        }
        if (interests[keys[i][j][z][0] - 1]?.scoreMax === undefined) {
          interests[keys[i][j][z][0] - 1] = {
            ...interests[keys[i][j][z][0] - 1],
            scoreMax: 0,
          };
        }

        if (i < 3) {
          interests[keys[i][j][z][0] - 1].score +=
            (Number(userAnswers[i][j]) / 10) * Number(keys[i][j][z][1]);
          interests[keys[i][j][z][0] - 1].scoreMax += Number(keys[i][j][z][1]);
        } else if (i < 14) {
          interests[keys[i][j][z][0] - 1].score +=
            (Number(userAnswers[i][j]) / 4) * Number(keys[i][j][z][1]);
          interests[keys[i][j][z][0] - 1].scoreMax += Number(keys[i][j][z][1]);
        } else {
          console.log(userAnswers[i]);
          console.log(i + " " + j + " " + z);
          if (userAnswers[i][0].includes(j.toString())) {
            interests[keys[i][j][z][0] - 1].score += Number(keys[i][j][z][1]);
          }
          interests[keys[i][j][z][0] - 1].scoreMax += Number(keys[i][j][z][1]);
        }
      }
    }
  }

  interests.sort((a,b) => (b.score / b.scoreMax) - (a.score / a.scoreMax))
  const interestsBox = document.getElementById("interests-box");
  for (let i = 0; i < interests.length; i++) {
    const interestRecord = document.createElement("p");
    interestRecord.innerText = `${i + 1}. ${interests[i].name}: ${Math.round(
      (interests[i].score / interests[i].scoreMax) * 100
    )}%`;
    emailText += "\n" + interestRecord.innerText;
    interestsBox.appendChild(interestRecord);

    for (let j = 0; j < interests[i].profType.length; j++) {
      profTypes[interests[i].profType[j][0] - 1].score +=
        interests[i].score * interests[i].profType[j][1];
      profTypes[interests[i].profType[j][0] - 1].scoreMax +=
        interests[i].scoreMax * interests[i].profType[j][1];
    }
  }

  const proftypesBox = document.getElementById("proftypes-box");
  for (let i = 0; i < profTypes.length; i++) {
    const proftypeRecord = document.createElement("p");
    proftypesBox.appendChild(proftypeRecord);
    proftypeRecord.innerText = `${profTypes[i].name}: ${Math.round(
      (profTypes[i].score / profTypes[i].scoreMax) * 100
    )}%`;
    emailText += "\n" + proftypeRecord.innerText;
  }
}


